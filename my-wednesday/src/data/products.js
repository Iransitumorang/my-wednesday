export const getProductById = (id) => products.find((p) => p.id === Number(id))
export const getProductsByCategory = (cat) =>
  cat ? products.filter((p) => p.category === cat) : products
export const searchProducts = (q, items = products) => {
  if (!q?.trim()) return items
  const lower = q.trim().toLowerCase()
  return items.filter(
    (p) =>
      p.name.toLowerCase().includes(lower) || p.description.toLowerCase().includes(lower)
  )
}
export const getBestSellers = () =>
  [...products].sort((a, b) => (b.soldCount || 0) - (a.soldCount || 0))
